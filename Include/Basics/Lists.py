# 负索引表示从末尾开始，-1 表示最后一个项目，-2 表示倒数第二个项目，依此类推。
friends = ["Kevin", "Karen", "Jim", "Jam", "Landy"]

print(friends)

print(friends[0] + " and " + friends[3] + " are best friends!")

print(friends[-1])

print(friends[1:4])
# modify the value in the index equal to 1
friends[1] = "Mike"

print(friends)

